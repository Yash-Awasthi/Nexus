export function FeaturesSection() {
  const gridRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    let cancelled = false;
    Promise.all([import("gsap"), import("gsap/ScrollTrigger")]).then(
      ([{ default: gsap }, { ScrollTrigger }]) => {
        if (cancelled) return;
        gsap.registerPlugin(ScrollTrigger);

        if (gridRef.current) {
          gsap.fromTo(
            Array.from(gridRef.current.children),
            { opacity: 0, y: 30 },
            {
              opacity: 1,
              y: 0,
              duration: 0.6,
              stagger: 0.08,
              ease: "power2.out",
              scrollTrigger: { trigger: gridRef.current, start: "top 75%" },
            }
          );
        }
      }
    );
    return () => { cancelled = true; };
  }, []);

  return (
    <section id="features" className="relative py-24 sm:py-32">
      <div className="mx-auto max-w-7xl px-4 sm:px-6 lg:px-8">
        <div className="text-center mb-16">
          <h2 className="text-3xl sm:text-4xl font-bold text-white tracking-tight">
            Built for <span className="text-[#4de8ff]">Deeper Thinking</span>
          </h2>
          <p className="mt-4 text-base text-white/40 max-w-xl mx-auto">
            Everything you need to orchestrate multi-perspective AI deliberations at scale.
          </p>
        </div>

        <div ref={gridRef} className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4">
          {features.map((f) => (
            <div
              key={f.title}
              className="group relative bg-white/[0.03] backdrop-blur-sm border border-white/5 rounded-xl p-6 hover:border-[#4d7cff]/20 hover:bg-white/[0.05] transition-all duration-300 opacity-0"
            >
              <div className="flex items-center justify-center w-10 h-10 rounded-lg bg-[#4d7cff]/10 text-[#4d7cff] mb-4 group-hover:bg-[#4d7cff]/15 transition-colors">
                {f.icon}
              </div>
              <h3 className="text-base font-semibold text-white">{f.title}</h3>
              <p className="mt-2 text-sm text-white/40 leading-relaxed">{f.desc}</p>
            </div>
          ))}
        </div>
      </div>
    </section>
  );
}
